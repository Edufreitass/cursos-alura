/**
 * Compila e imprime o alfabeto pois caracteres são números em Java.
 */
class A {

    // Compila e imprime o alfabeto entre a e z, inclusive.
    public static void main(String[] args) {
        for (char c = 'a'; c <= 'z'; c++) {
            System.out.println(c);
        }
    }
}
